    public WindowSelectDB(DBConnection conn){
        try {
            this.conn = conn;
            databases = new ArrayList<>();
            ResultSet result = conn.query("SELECT SCHEMA_NAME FROM INFORMATION_SCHEMA.SCHEMATA");
            while(result.next()){
                String database = result.getString("SCHEMA_NAME");
                databases.add(database);
            }
            initComponents();
        } catch (SQLException ex) {
            Logger.getLogger(WindowSelectDB.class.getName()).log(Level.SEVERE, null, ex);
        }
        cb_sourceDB.setModel(new DefaultComboBoxModel(databases.toArray()));
        cb_destDB.setModel(new DefaultComboBoxModel(databases.toArray()));
    }

