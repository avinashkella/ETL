    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ArrayList<String> sourceTables = new ArrayList<>();
        ArrayList<String> destTables = new ArrayList<>();
        try {
            p = new Process();
            
            String sourceDB = cb_sourceDB.getSelectedItem().toString();
            String destDB = cb_destDB.getSelectedItem().toString();
            
            p.setSourceDB(sourceDB);
            p.setDestDB(destDB);
            String sql1 = "SELECT TABLE_NAME FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_SCHEMA = '" + sourceDB + "' AND TABLE_TYPE = 'base table'";
            String sql2 = "SELECT TABLE_NAME FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_SCHEMA = '" + destDB + "' AND TABLE_TYPE = 'base table'";
            
            ResultSet res1 = conn.query(sql1);
            ResultSet res2 = conn.query(sql2);
            
            while(res1.next()){
                sourceTables.add(res1.getString("TABLE_NAME").toString());
            }
            while(res2.next()){
                destTables.add(res2.getString("TABLE_NAME").toString());
            }
            
            //System.out.println(sourceTables.toString());
            //System.out.println(destTables.toString());
        } catch (SQLException ex) {
            Logger.getLogger(WindowSelectDB.class.getName()).log(Level.SEVERE, null, ex);
        }
        WindowSelectTables w = new WindowSelectTables(sourceTables, destTables, p);
        w.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

